export type TranslationKey = keyof typeof dictionaries[typeof DEFAULT_LANGUAGE];
export type Translator = (key: TranslationKey) => string;

export function resolveLanguage(value: string | null | undefined): Language {
  return value === "en-US" || value === "zh-CN" ? value : DEFAULT_LANGUAGE;
}

export function createTranslator(language: string | null | undefined): Translator {
  const resolvedLanguage = resolveLanguage(language);
  const dictionary = dictionaries[resolvedLanguage];
  const fallbackDictionary = dictionaries[DEFAULT_LANGUAGE];

  return (key) => dictionary[key] ?? fallbackDictionary[key] ?? key;
}
